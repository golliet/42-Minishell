#include "minishell.h"

void	ft_echo(char **t, char *str)
{
	if (!t[1])
		ft_putendl("");
	else if (!t[2] && t[1][0] == '-' && t[1][1] == 'n')
		;
	else if (ft_strcmp(t[1], "-n") == 0)
	{
		ft_putstr(str + 8);
		write(1, "\033[7;40m%\n", 9);
		write(1, "\033[0m", 4);
	}
	else
		ft_putendl(str + 5);
}
