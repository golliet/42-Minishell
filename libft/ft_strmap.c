#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char				*str;
	unsigned int		i;

	if (s == NULL || f == NULL)
		return (NULL);
	i = 0;
	str = ft_strnew(ft_strlen(s));
	if (str == NULL)
		return (0);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	return (str);
}
